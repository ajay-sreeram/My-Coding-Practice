public class Main
{
public static void main(String args[])
{
System.out.println("It is STRING main");
}
public static void main(int a[])
{
System.out.println("it is INT main");
}
public static void main()
{
System.out.println("it is VOID main");
}
}

/*there can be any number of main methods in single class
 with diff number of parameters but in default main with String array is called*/